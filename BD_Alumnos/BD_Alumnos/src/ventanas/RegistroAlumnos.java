        //Codigo que permite insertar registros en al base de datos
        try{                                                            //nombre de la base de datos,   
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/universidad_lab4", "root", "admin");
            //variable de tipo de conexcion a base de datos a que inserte
            PreparedStatement pst = cn.prepareStatement("insert into maestro values(?,?,?,?,?,?)");
            
            //pone 1, 2, 3 de los campos donde le puse ?
            pst.setString(1, txt_idmaestro.getText().trim());//como es increment se pone un cero y la maquina pone el 1 
            pst.setString(2, txt_nombre.getText().trim());
            pst.setString(3, txt_apellido.getText().trim());
            pst.setString(4, txt_edad.getText().trim());
            pst.setString(5, txt_direccion.getText().trim());
            pst.setString(6, txt_estadomaestro.getText().trim());
            pst.executeUpdate();
            
            txt_idmaestro.setText("");
            txt_nombre.setText("");
            txt_apellido.setText("");
            txt_edad.setText("");
            txt_direccion.setText("");
            txt_estadomaestro.setText("");
          
            label_status.setText("Registro exitoso.");
        }catch (Exception e){
            
        }
        
