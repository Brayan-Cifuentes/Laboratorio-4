    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Codigo que permite consultar registros en la base de datos
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/universidad_lab4", "root", "admin");
            PreparedStatement pst = cn.prepareStatement("select * from maestro where ID = ?");
            pst.setString(1, txt_buscar.getText().trim());
            
            ResultSet rs = pst.executeQuery();
            // si el eof o el siguiente
            if(rs.next()){
                txt_nombre.setText(rs.getString("NombreAlumno"));
                txt_apellido.setText(rs.getString("Apellido"));
                txt_edad.setText(rs.getString("NombreAlumno"));
                txt_direccion.setText(rs.getString("Apellido"));
                txt_estadomaestro.setText(rs.getString("Apellido"));
            } else {
                JOptionPane.showMessageDialog(null, "Alumno no registrado.");
            }
            
        }catch (Exception e){
            
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Codigo que permite actualizar registros en la base de datos
        try {
            String ID = txt_buscar.getText().trim();
            
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/universidad_lab4", "root", "admin");
            PreparedStatement pst = cn.prepareStatement("update maestro set ID_Maestro = ?, Nombre_Maestro = ?, Apellido_Maestro=?, Edad=?, Direcccion_Maestro=?, Estado_Maestro=?, where ID = " + ID);
            
            pst.setString(1, txt_nombre.getText().trim());
            pst.setString(2, txt_apellido.getText().trim());
            pst.setString(3, txt_edad.getText().trim());
            pst.setString(4, txt_direccion.getText().trim());
            pst.setString(5, txt_estadomaestro.getText().trim());
            
            pst.executeUpdate();
            
            label_status.setText("Modificaci√≥n exitosa.");
            
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

